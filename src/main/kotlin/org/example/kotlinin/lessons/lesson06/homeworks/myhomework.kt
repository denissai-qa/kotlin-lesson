//Задание 4: "Расчет бонусных баллов"
//Клиенты интернет-магазина получают бонусные баллы за покупки. Напишите функцию, которая принимает
//сумму покупки и печатает в консоль количество бонусных баллов: 2 балла за каждые 100 рублей при
//сумме покупки до 1000 рублей и 3 балла за каждые 100 рублей при сумме свыше этого.

fun bonusPoints(purchaseAmount: Int) {
    val points = if (purchaseAmount <= 1000) {
        (purchaseAmount / 100) * 2
    } else {
        (purchaseAmount / 100) * 3
    }
    println("Сумма покупки: $purchaseAmount руб. → Бонусные баллы: $points")
}
