//Задание 2: "Расчет возраста питомца"
//Создайте функцию, которая преобразует возраст собаки в "человеческие" годы.
//До 2 лет каждый год собаки равен 10.5 человеческим годам, после - каждый год равен
//4 человеческим годам. Результат распечатай в консоль.

fun dogToHumanAge(dogAge: Int) {
    val humanAge = if (dogAge <= 2) {
        dogAge * 10.5
    } else {
        2 * 10.5 + (dogAge - 2) * 4
    }
    println("Возраст собаки $dogAge года(лет) = $humanAge человеческих лет")
}
